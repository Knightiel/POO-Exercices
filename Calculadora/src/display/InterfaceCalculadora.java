    private String realizaConta(int num1, int num2, String op){
        Integer conta;
        String resultado;
        switch (op) {
            case "+":
                conta = num1 + num2;
                resultado = conta.toString();
                return resultado;
        
            case "-":
                conta = num1 - num2;
                resultado = conta.toString();
                return resultado;
            
            case "*":
                conta = num1 * num2;
                resultado = conta.toString();
                return resultado;
            
            case "/":
                if(num2 == 0){
                    resultado = "NaN";
                    return resultado;
                }else{
                    conta = num1 / num2;
                    resultado = conta.toString();
                    return resultado;
                }
        }
    }

